let currentLang = localStorage.getItem("clockit_lang") || "en";
 
function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || translations.en[key] || key;
}
 
function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("clockit_lang", lang);
  translatePage();
}
 
function toggleLang() {
  setLang(currentLang === "en" ? "el" : "en");
}
 
function getLang() {
  return currentLang;
}
 
function translatePage() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  const langBtn = document.getElementById("lang-toggle");
  if (langBtn) langBtn.textContent = t("lang_toggle");
}
 
window.i18n = { t, setLang, toggleLang, getLang, translatePage };